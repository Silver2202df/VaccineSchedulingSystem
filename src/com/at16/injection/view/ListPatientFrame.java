    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int row = patientTable.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(ListPatientFrame.this, "Vui lòng chọn một đối tượng", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else{
            int confirm = JOptionPane.showConfirmDialog(ListPatientFrame.this, "Bạn chắc chắn muốn xóa không?");
            if(confirm == JOptionPane.YES_OPTION){
                String patientLicense = String.valueOf(patientTable.getValueAt(row, 3));
                
                patientService.deletePatient(patientLicense);
                defaultTableModel.setRowCount(0);
                setTableData(patientService.getAllPatients());
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
       int row = patientTable.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(ListPatientFrame.this, "Vui lòng chọn một đối tượng", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }else{
            String licensePatient = String.valueOf(patientTable.getValueAt(row, 3));
            
            new EditPatientFrame(licensePatient).setVisible(true);
        }
    }//GEN-LAST:event_editButtonActionPerformed

    
