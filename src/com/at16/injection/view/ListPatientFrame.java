    private void setTableData(List<Patient> patients){
        for(Patient patient : patients){
            defaultTableModel.addRow(new Object[]{patient.getFullName(), patient.getAddress(), patient.getDob()
                    , patient.getLicense(), patient.getPhone(),patient.getSex(), patient.getObjectCode()
                    ,  patient.getBackgroundIllness(), patient.getDateInjection(), patient.getShift(), patient.getNumber()});
        }
    }

